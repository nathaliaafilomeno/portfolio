const STORAGE_KEY = 'nf-lang';

function applyLanguage(lang) {
  const dict = translations[lang] || translations.pt;

  document.documentElement.lang = lang === 'en' ? 'en' : 'pt-BR';

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) el.textContent = dict[key];
  });

  document.querySelectorAll('[data-i18n-html]').forEach((el) => {
    const key = el.getAttribute('data-i18n-html');
    if (dict[key] !== undefined) el.innerHTML = dict[key];
  });

  document.querySelectorAll('[data-i18n-alt]').forEach((el) => {
    const key = el.getAttribute('data-i18n-alt');
    if (dict[key] !== undefined) el.alt = dict[key];
  });

  const titleEl = document.querySelector('title[data-i18n]');
  if (titleEl && dict['meta.title']) document.title = dict['meta.title'];

  const metaDesc = document.querySelector('meta[name="description"][data-i18n-content]');
  if (metaDesc && dict['meta.description']) metaDesc.setAttribute('content', dict['meta.description']);

  document.querySelectorAll('[data-lang-btn]').forEach((btn) => {
    btn.classList.toggle('is-active', btn.getAttribute('data-lang-btn') === lang);
  });
}

export function initI18n() {
  const stored = localStorage.getItem(STORAGE_KEY);
  const initial = stored === 'en' || stored === 'pt' ? stored : 'pt';
  applyLanguage(initial);

  document.querySelectorAll('[data-lang-btn]').forEach((btn) => {
    btn.addEventListener('click', () => {
      const lang = btn.getAttribute('data-lang-btn');
      localStorage.setItem(STORAGE_KEY, lang);
      applyLanguage(lang);
    });
  });
}
